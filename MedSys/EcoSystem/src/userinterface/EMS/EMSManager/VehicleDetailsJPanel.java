    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        logger.log(Level.INFO, "btnBackActionPerformed --VehicleDetailsJPanel");

        userProcessContainer.remove(this);
        CardLayout cardLayout = (CardLayout) userProcessContainer.getLayout();
        cardLayout.previous(userProcessContainer);
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        Component[] comps = this.userProcessContainer.getComponents();
        for (Component comp : comps) {
            if (comp instanceof ManageAmbulanceServiceJPanel) {
                ManageAmbulanceServiceJPanel createP = (ManageAmbulanceServiceJPanel) comp;
                createP.populateTable();
            }
            layout.previous(userProcessContainer);
        }

    }//GEN-LAST:event_btnBackActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        logger.log(Level.INFO, "btnSaveActionPerformed --VehicleDetailsJPanel");

        if (ComboBoxLocality.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Please choose a locality");
        }
        String locality = String.valueOf(ComboBoxLocality.getSelectedItem());

        boolean vehicleAvailability;
        boolean maintenanceCertificate;

        if (btnYesVA.isSelected()) {
            btnYesVA.setSelected(true);
            vehicleAvailability = true;

        } else {
            btnNoVA.setSelected(true);
            vehicleAvailability = false;

        }

        vehicle.setAvailable(vehicleAvailability);
        vehicle.setAvailable_locality(locality);

        JOptionPane.showMessageDialog(null, "The entered Vehicle details have been saved successfully!");

        btnUpdate.setEnabled(true);
        txtVehicleNo.setEnabled(false);
        txtModel.setEnabled(false);
        txtSerialNo.setEnabled(false);
        btnYesVA.setEnabled(false);
        btnNoVA.setEnabled(false);
        ComboBoxManufacturedYear.setEnabled(false);
        ComboBoxLocality.setEnabled(false);
        btnSave.setEnabled(false);


    }//GEN-LAST:event_btnSaveActionPerformed

