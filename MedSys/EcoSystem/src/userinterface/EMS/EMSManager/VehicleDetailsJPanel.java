    public void populateDetails() {
        logger.log(Level.INFO, "populateDetails --VehicleDetailsJPanel");

        ComboBoxManufacturedYear.setSelectedItem(vehicle.getManufactured_year());

        txtSerialNo.setText(vehicle.getSerial_num());
        txtVehicleNo.setText(vehicle.getVehicleNo());
        ComboBoxLocality.setSelectedItem(vehicle.getAvailable_locality());
        txtModel.setText(vehicle.getModel());

        if (vehicle.isAvailable() == true) {
            btnYesVA.setSelected(true);
        } else {
            btnNoVA.setSelected(true);
        }

        txtVehicleNo.setEnabled(false);
        txtModel.setEnabled(false);
        txtSerialNo.setEnabled(false);
        btnYesVA.setEnabled(false);
        btnNoVA.setEnabled(false);
        ComboBoxManufacturedYear.setEnabled(false);
        ComboBoxLocality.setEnabled(false);
        btnSave.setEnabled(false);
    }

