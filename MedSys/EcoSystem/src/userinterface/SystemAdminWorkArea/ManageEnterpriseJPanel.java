    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        logger.log(Level.INFO, "submitJButtonActionPerformed --ManageEnterprise");

        if (nameJTextField.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter valid name !");
            return;
        }

        Network network = (Network) networkJComboBox.getSelectedItem();
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) enterpriseTypeJComboBox.getSelectedItem();

        if (network == null || type == null) {
            JOptionPane.showMessageDialog(null, "Invalid Input!");
            return;
        }

        String name = nameJTextField.getText();

        for (Enterprise e : network.getEnterpriseDirectory().getEnterpriseList()) {
            if (name.equalsIgnoreCase(e.getName())) {
                JOptionPane.showMessageDialog(null, "Enterprise already exits!", "warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }

        String mailID = mail.getText();

        if (!mailID.isEmpty()) {
            Pattern VALID_EMAIL_ADDRESS_REGEX
                    = Pattern.compile("^[A-Z0-9._%+-]+@[A-Z0-9.-]+\\.[A-Z]{2,6}$", Pattern.CASE_INSENSITIVE);
            Matcher matcher = VALID_EMAIL_ADDRESS_REGEX.matcher(mailID);

            if (!matcher.matches()) {
                JOptionPane.showMessageDialog(userProcessContainer, "Please enter valid email ID");
                return;
            }
        }

        Enterprise enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(name, type, mailID);

        for (Enterprise e
                : network.getEnterpriseDirectory()
                        .getEnterpriseList()) {
            if (name.equalsIgnoreCase(e.getMailID())) {
                JOptionPane.showMessageDialog(null, "MailID already exits!", "warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }

        JOptionPane.showMessageDialog(
                null, "Enterprise has been added successfully", "success", JOptionPane.PLAIN_MESSAGE);
        populateTable();

        nameJTextField.setText(
                "");
        mail.setText(
                "");
        networkJComboBox.setSelectedIndex(
                0);
        enterpriseTypeJComboBox.setSelectedIndex(
                0);

    }//GEN-LAST:event_submitJButtonActionPerformed

