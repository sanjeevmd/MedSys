    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        logger.log(Level.INFO, "btnDeleteActionPerformed --ManageEnterprise");
        int selectedrow = enterpriseJTable.getSelectedRow();
        if (selectedrow >= 0) {
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the enterprise?", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                Enterprise enterprise = (Enterprise) enterpriseJTable.getValueAt(selectedrow, 0);
                for (Network network : system.getNetworkList()) {
                    this.enterpriseDirectory = network.getEnterpriseDirectory();
                }
                enterpriseDirectory.removeEnterprise(enterprise);
                JOptionPane.showMessageDialog(null, "Deleted successfully");
                populateTable();
            }

        } else {
            JOptionPane.showMessageDialog(null, "Please select a row");
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

