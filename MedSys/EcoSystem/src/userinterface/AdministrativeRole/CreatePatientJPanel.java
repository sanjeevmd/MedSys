    private void btnPatientSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPatientSubmitActionPerformed
        // TODO add your handling code here:  
        logger.log(Level.INFO, "btnPatientSubmitActionPerformed --createpatient");

        String patientHRN = txtPatientHRN.getText();

        if (txtPatientName.getText().length() == 0) {
            JOptionPane.showMessageDialog(userProcessContainer, "Please enter Name");
            return;
        }
        if (patientHRN.length() == 0) {
            JOptionPane.showMessageDialog(userProcessContainer, "Please enter HRN");
            return;
        }
        for (Patient pat : enterprise.getPatientDirectory().getPatientList()) {
            if (pat.getPatientHRN().equals(patientHRN)) {
                JOptionPane.showMessageDialog(userProcessContainer, "HRN already exist");
                return;
            }
        }
        if (txtPatientName.getText().matches(".*\\d.*")) {
            JOptionPane.showMessageDialog(userProcessContainer, "Please enter alphabets");
            return;
        }

        if (jComboBox1.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(userProcessContainer, "Please choose a blood group");
            return;
        }
        Patient patient = enterprise.getPatientDirectory().addPatient();
        patient.setPatientName(txtPatientName.getText());
        patient.setPatientHRN(patientHRN);
        patient.setBloodGroup(jComboBox1.getSelectedItem().toString());
        JOptionPane.showMessageDialog(userProcessContainer, "Patient Created");
        new Thread(new Runnable() {
            public void run() {
                try {
                    SendMail.sendMail(enterprise.getMailID(), "Hi, Patient Created Alert \n\n" + "Patient Name : " + patient.getPatientName() + "\nPatient HRN : " + patient.getPatientHRN()
                            + "\nBlood Group : " + patient.getBloodGroup(), patient.getPatientHRN());
                } catch (MessagingException ex) {
                    Logger.getLogger(CreatePatientJPanel.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }).start();

        txtPatientHRN.setText("");
        txtPatientName.setText("");
        jComboBox1.setSelectedIndex(0);
    }//GEN-LAST:event_btnPatientSubmitActionPerformed

